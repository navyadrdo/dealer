package com.dealer.management.model;

public class Student {
	
	private Integer Sno=null;
	private String name=null;
	private String postel=null;
	private String personal=null;
	
	
	
	
	
	private Integer getSno() {
		return Sno;
	}
	private void setSno(Integer sno) {
		Sno = sno;
	}
	private String getName() {
		return name;
	}
	private void setName(String name) {
		this.name = name;
	}
	private String getPostel() {
		return postel;
	}
	private void setPostel(String postel) {
		this.postel = postel;
	}
	private String getPersonal() {
		return personal;
	}
	private void setPersonal(String personal) {
		this.personal = personal;
	}
	
	
	
	
	
	

}
